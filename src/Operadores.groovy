// Operadores.groovy

// Operadores aritméticos
def a = 10
def b = 5
println "Suma: ${a + b}"
println "Resta: ${a - b}"
println "Multiplicacion: ${a * b}"
println "Division: ${a / b}"

// Operadores relacionales
println "a es mayor que b?: ${a > b}"
println "a es igual a b?: ${a == b}"
println "a es mayor que b?: ${a < b}"
println "a es mayor igual que b?: ${a >= b}"

// Operadores lógicos
def x = true
def y = false
println "x AND y: ${x && y}"
println "x OR y: ${x || y}"
println "NOT x: ${!x}"